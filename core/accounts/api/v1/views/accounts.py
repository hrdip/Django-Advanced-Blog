# Password Reset
class RequestPasswordReset(generics.GenericAPIView):
    queryset = User.objects.all()
    serializer_class = ResetPasswordViaEmailSerializer

    def post(self, request):
        serializer = self.serializer_class(data=request.data)
        email = request.data["email"]
        if User.objects.filter(email=email).exists():
            user = User.objects.get(email=email)
            uidb64 = urlsafe_base64_encode(smart_bytes(user.id))
            token = PasswordResetTokenGenerator().make_token(user)
            current_site = get_current_site(request).domain
            realtivelink = reverse("password-reset")
            email_obj = EmailMessage(
                "email/reset_password_email.tpl",
                {"token": token, "uidb64": uidb64},
                "admin@admin.com",
                to=[email],
            )
            EmailThread(email_obj).start()
            return Response(
                {
                    "details": "request for pssword reset successfuly send to your email"
                },
                status=status.HTTP_200_OK,
            )


class PasswordTokenCheckAPIView(generics.GenericAPIView):
    def get(self, request, uidb64, token):
        try:
            id = smart_str(urlsafe_base64_decode(uidb64))
            user = User.objects.get(id=id)
            if not PasswordResetTokenGenerator().check_token(user, token):
                return Response(
                    {
                        "detail": "token is not valid, please check the new one"
                    },
                    status=status.HTTP_401_UNAUTHORIZED,
                )
            return Response(
                {
                    "details": "Credential Valid",
                    "uidb64": uidb64,
                    "token": token,
                },
                status=status.HTTP_200_OK,
            )
        except DjangoUnicodeDecodeError as indentifier:
            return Response(
                {"details": "token is not valid, please check the new one"},
                status=status.HTTP_401_UNAUTHORIZED,
            )


